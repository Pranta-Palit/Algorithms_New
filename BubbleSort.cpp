///................................................Code...................................................................
#include<bits/stdc++.h>
using namespace std;
#define MAX 1000
int arr[MAX];
int main()
{
///.............................................Variable................................................................
    int n,Minimum,temp,j,i,k;
    scanf("%d",&n);
///.............................................Input in Array......................................................
    for( k=0; k<n; k++)
        scanf("%d",&arr[k]);
///............................................Sorting.................................................................
    for( i=0; i<n; i++)
    {
        for(j=0; j<=n-2; j++)
        {
            if(arr[j]>arr[j+1])
            {
                /// Swapping Now
                temp = arr[j];
                arr[j] = arr[j+1];
                arr[j+1] = temp;
            }
        }
    }
///.........................................................Output...................................................
    for(int k=0; k<n; k++)
        printf("%d ",arr[k]);
    printf("\n");
///.........................................................END........................................................
    return 0;
}

