///................................................Code...................................................................
#include<bits/stdc++.h>
using namespace std;
#define MAX 1000
int arr[MAX];
int main()
{
///.............................................Variable................................................................
    int n,Minimum,temp,j,i,k;
    scanf("%d",&n);
///.............................................Input in Array......................................................
    for( k=0; k<n; k++)
        scanf("%d",&arr[k]);
///............................................Sorting.................................................................
    for( i=0; i<n; i++)
    {
        Minimum = i;
        for(j = i+1; j<n; j++)
        {
            if(arr[j]<arr[Minimum])
                Minimum = j;
        }
        /// Swapping Now
        temp = arr[i];
        arr[i] = arr[Minimum];
        arr[Minimum] = temp;
    }
///.........................................................Output...................................................
    for(int k=0; k<n; k++)
        printf("%d ",arr[k]);
    printf("\n");
///.........................................................END........................................................

    return 0;
}
